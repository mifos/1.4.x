DROP TABLE SURVEY_RESPONSE_FREETEXT;
DROP TABLE SURVEY_RESPONSE_CHOICE;
DROP TABLE SURVEY_RESPONSE_DATE;
DROP TABLE SURVEY_RESPONSE_NUMBER;

CREATE TABLE SURVEY_RESPONSE (
  RESPONSE_ID INTEGER AUTO_INCREMENT NOT NULL,
  INSTANCE_ID INTEGER NOT NULL,
  QUESTION_ID INTEGER NOT NULL,

  FREETEXT_VALUE TEXT,
  CHOICE_VALUE INTEGER,
  DATE_VALUE DATE,
  NUMBER_VALUE DECIMAL(16,5),

  UNIQUE(INSTANCE_ID, QUESTION_ID),
  PRIMARY KEY(RESPONSE_ID),
  FOREIGN KEY(QUESTION_ID)
    REFERENCES QUESTIONS(QUESTION_ID)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION,
  FOREIGN KEY(INSTANCE_ID)
    REFERENCES SURVEY_INSTANCE(INSTANCE_ID)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION,
  FOREIGN KEY(CHOICE_VALUE)
    REFERENCES QUESTION_CHOICES(CHOICE_ID)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION
);


